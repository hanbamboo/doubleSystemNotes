package com.shuangti.basic;

public class Hello {
	public static void main(String[] args) {
		System.out.println("Hello World");
	}
}

/*
 * public 公共的
 *  class 类 
 *  Hello 类名 
 *  类名后{}代表类的作用域
 *   static 静态的 
 *   void 空 
 *   main 方法名 
 *   String[] args 形参列表 
 *   方法名后的{}代表方法的作用域 
 *   System.out.println("Hello World"); 
 *   将()内 的内容打印到控制台
 *  
 *   控制台，console窗口
 *   作用域都是一对对花括号
 *   每行代码用英文的分号
 *   方法、类不是用分号结束的，符号都有固定用法
 *   类、方法就只用{}
 */