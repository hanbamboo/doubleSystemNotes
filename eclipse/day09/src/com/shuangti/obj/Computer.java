package com.shuangti.obj;

public class Computer {

	public void jia(int a, int b) {
		System.out.println(a + "+" + b + "=" + (a + b));
	}

	public void jian(int a, int b) {
		System.out.println(a + "-" + b + "=" + (a - b));
	}

	public void cheng(int a, int b) {
		System.out.println(a + "*" + b + "=" + (a * b));
	}

	public void chu(int a, int b) {
		System.out.println(a + "/" + b + "=" + (a / b));
	}
}
