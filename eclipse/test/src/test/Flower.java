package test;

public class Flower {
	private int id; //花花id
	private String name; //花花名字
	private int shoucang; //收藏标识，1代表收藏，0代表不收藏
	
	
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getShoucang() {
		return shoucang;
	}
	public void setShoucang(int shoucang) {
		this.shoucang = shoucang;
	}
	
	
}
